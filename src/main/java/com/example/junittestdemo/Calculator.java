package com.example.junittestdemo;

public class Calculator {
    public int doAddition(int a,int b){
        return a+b;
    }

    public int doSubtraction(int a,int b){
        return a-b;
    }

    public int doMultiplication(int a,int b){
        return a*b;
    }
    public int doDivision(int a,int b){
        return a/b;
    }
}
